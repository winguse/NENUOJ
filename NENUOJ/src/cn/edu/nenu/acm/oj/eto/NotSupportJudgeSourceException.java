package cn.edu.nenu.acm.oj.eto;

public class NotSupportJudgeSourceException extends Exception {

	public NotSupportJudgeSourceException() {
	}

	public NotSupportJudgeSourceException(String arg0) {
		super(arg0);
	}

	public NotSupportJudgeSourceException(Throwable arg0) {
		super(arg0);
	}

	public NotSupportJudgeSourceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotSupportJudgeSourceException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
